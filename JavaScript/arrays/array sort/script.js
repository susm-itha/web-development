//array sort 
let numbers = [5, 2, 9, 1];
console.log(numbers.sort());

//array reverse
let letters = ["A", "B", "C", "D"];
letters.reverse();
console.log(letters); // ["D", "C", "B", "A"]

//array tosorted 
let fruits = ["Banana", "Apple", "Cherry"];
let Fruits = fruits.toSorted();
console.log(Fruits);

//array reversed
let reversed = numbers.toReversed();
console.log("Original:", numbers);  
console.log("Reversed:", reversed);